
public class Instructor {
	
	private String name;
	private String CCA;

	public Instructor(String name, String CCA) {
		this.name = name;
		this.CCA = CCA;
		
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getCCA() {
		return CCA;
	}

	public void setCCA(String CCA) {
		this.CCA = CCA;
	}
	
	
	

}
